function setLang(lang) {
  const dict = translations[lang] || translations.fr;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) el.setAttribute("placeholder", dict[key]);
  });
  const langBtn = document.querySelector("#langToggle");
  if (langBtn) {
    langBtn.textContent = lang === "fr" ? "FR | EN" : "EN | FR";
    langBtn.setAttribute("aria-label", lang === "fr" ? "Changer la langue" : "Change language");
  }
  localStorage.setItem("bf_lang", lang);
}

function bindLangToggle() {
  const btn = document.querySelector("#langToggle");
  if (!btn) return;
  btn.addEventListener("click", () => {
    const current = localStorage.getItem("bf_lang") || "fr";
    setLang(current === "fr" ? "en" : "fr");
  });
  setLang(localStorage.getItem("bf_lang") || "fr");
}

window.addEventListener("DOMContentLoaded", bindLangToggle);
